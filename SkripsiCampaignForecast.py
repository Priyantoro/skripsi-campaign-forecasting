param_grid = {
    "changepoint_prior_scale": [0.001, 0.01, 0.05, 0.1, 0.5],
    "seasonality_mode": ["additive", "multiplicative"],
}
all_params = [
    dict(zip(param_grid.keys(), v)) for v in product(*param_grid.values())
]
regressors = ["spend", "impressions", "holiday", "is_weekend"]
predict_cols = ["ds", "cap", "floor"] + regressors


# 2. Bungkus Logika Tuning ke dalam Fungsi (DRY Principle) ⭐
def tune_prophet(test_windows):
    tuning_results = []
    for params in all_params:
        fold_maes = []
        for train_df, test_df in test_windows:
            # Inisialisasi Model
            model = Prophet(
                growth="logistic",
                weekly_seasonality=True,
                yearly_seasonality=False,
                daily_seasonality=False,
                **params  # Menggunakan kwarg unpacking agar otomatis membaca isi dict params
            )

            # Tambah Regressors secara dinamis via loop
            for reg in regressors:
                model.add_regressor(reg)

            model.fit(train_df)

            # Prediksi dan Hitung Error
            forecast = model.predict(test_df[predict_cols])
            mae = mean_absolute_error(test_df["y"], forecast["yhat"])
            fold_maes.append(mae)

        tuning_results.append({"params": params, "mae": np.mean(fold_maes)})
    return tuning_results


