def tune_prophet_flexible(test_windows, growth="linear"):
    tuning_results = []

    for params in all_params:
        fold_maes = []

        for train_df, test_df in test_windows:
            # Inisialisasi Model secara fleksibel
            model = Prophet(
                growth=growth,  # 'linear' untuk Clicks
                weekly_seasonality=True,
                yearly_seasonality=False,
                daily_seasonality=False,
                **params
            )

            # Tambah Regressors
            for reg in regressors:
                model.add_regressor(reg)

            model.fit(train_df)

            # Secara dinamis mendeteksi kolom yang wajib masuk ke predict
            predict_cols = ["ds"] + regressors

            # Prediksi dan Hitung Error
            forecast = model.predict(test_df[predict_cols])
            mae = mean_absolute_error(test_df["y"], forecast["yhat"])
            fold_maes.append(mae)

        tuning_results.append({"params": params, "mae": np.mean(fold_maes)})

    return tuning_results

