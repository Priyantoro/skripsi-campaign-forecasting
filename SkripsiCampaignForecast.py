    ax.legend(loc='upper right')
    ax.grid(True, alpha=0.3)

    # =====================================================================
    # PLOT 3: Prediction Error & Residuals
    # =====================================================================
    ax = axes[2]

    residuals = actual - pred

    # Create scatter plot with color gradient
    colors = np.where(residuals >= 0, 'green', 'red')
