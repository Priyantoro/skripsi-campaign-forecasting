    ax.fill_between(range(len(actual)), actual_smooth, pred_smooth,
                    where=(actual_smooth >= pred_smooth),
                    color='green', alpha=0.1, label='Over-prediction')
    ax.fill_between(range(len(actual)), actual_smooth, pred_smooth,
                    where=(actual_smooth < pred_smooth),
                    color='red', alpha=0.1, label='Under-prediction')

