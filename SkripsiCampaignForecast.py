def prepare_data_for_horizon(data, metric, horizon):
    """
    Parameters:
    - data: DataFrame with time-series data
    - metric: 'CTR' or 'CPC'
    - horizon: number of steps ahead (1, 3, 7)

    Returns:
    - DataFrame with target variable created
    """
    data_copy = data.copy()

    # Generate target dynamically
    data_copy[f'{metric}_target'] = (
        data_copy
        .groupby('ad_set')[metric]
        .shift(-horizon)
    )

    # Remove rows with missing targets
    data_copy = data_copy.dropna(subset=[f'{metric}_target'])

    return data_copy


def create_time_series_split(data, train_ratio=0.8):
    split_idx = int(len(data) * train_ratio)
    train_data = data.iloc[:split_idx].copy()
    test_data = data.iloc[split_idx:].copy()
    return train_data, test_data


def train_and_evaluate(X_train, y_train, X_test, y_test, metric, horizon):
    """
    Parameters:
    - X_train, y_train: Training features and target
    - X_test, y_test: Testing features and target
    - metric: 'CTR' or 'CPC'
    - horizon: number of steps ahead

    Returns:
    - Dictionary with model, predictions, and metrics
    """
    # Scale features
    scaler = RobustScaler()
    X_train_scaled = scaler.fit_transform(X_train)
    X_test_scaled = scaler.transform(X_test)

    # Train model
    rf_model = RandomForestRegressor(**RF_PARAMS)
    rf_model.fit(X_train_scaled, y_train)

    # Make predictions
    y_pred_train = rf_model.predict(X_train_scaled)
    y_pred_test = rf_model.predict(X_test_scaled)

    # Calculate metrics
    mae_train = mean_absolute_error(y_train, y_pred_train)
    rmse_train = np.sqrt(mean_squared_error(y_train, y_pred_train))
    mape_train = mean_absolute_percentage_error(y_train, y_pred_train) * 100  # Convert to percentage
    r2_train = r2_score(y_train, y_pred_train)

    mae_test = mean_absolute_error(y_test, y_pred_test)
    rmse_test = np.sqrt(mean_squared_error(y_test, y_pred_test))
    mape_test = mean_absolute_percentage_error(y_test, y_pred_test) * 100  # Convert to percentage
    r2_test = r2_score(y_test, y_pred_test)

    # Store results
    result = {
        'metric': metric,
        'horizon': horizon,
        'train_mae': mae_train,
        'train_rmse': rmse_train,
        'train_mape': mape_train,
        'train_r2': r2_train,
        'test_mae': mae_test,
        'test_rmse': rmse_test,
        'test_r2': r2_test,
        'test_mape': mape_test,
        'train_size': len(y_train),
        'test_size': len(y_test),
        'y_test': y_test,
        'model': rf_model,
        'scaler': scaler,
        'y_pred':y_pred_test,
        'predictions': y_pred_test,
        'actuals': y_test.values,
    }

    return result


