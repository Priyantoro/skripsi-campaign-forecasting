from itertools import product
import numpy as np
from prophet import Prophet
from sklearn.metrics import mean_absolute_error

# 1. Definisikan Parameter Grid
param_grid = {
    "changepoint_prior_scale": [0.001, 0.01, 0.05, 0.1, 0.5],
    "seasonality_mode": ["additive", "multiplicative"],
}
all_params = [
    dict(zip(param_grid.keys(), v)) for v in product(*param_grid.values())
]
regressors = ["spend", "impressions", "holiday", "is_weekend"]


# Fungsi yang diperbarui agar fleksibel untuk CTR dan CPC ⭐
def tune_prophet_flexible(test_windows, growth="linear"):
    tuning_results = []

    for params in all_params:
        fold_maes = []

        for train_df, test_df in test_windows:
            # Inisialisasi Model secara fleksibel
            model = Prophet(
                growth=growth,  # 'linear' untuk CPC, 'logistic' untuk CTR
                weekly_seasonality=True,
                yearly_seasonality=False,
                daily_seasonality=False,
                **params
            )

            # Tambah Regressors
            for reg in regressors:
                model.add_regressor(reg)

            model.fit(train_df)

            # Secara dinamis mendeteksi kolom yang wajib masuk ke predict
            # Jika menggunakan 'logistic', cap dan floor otomatis ditambahkan ke list
            predict_cols = ["ds"] + regressors
            if growth == "logistic":
                predict_cols += ["cap", "floor"]

            # Prediksi dan Hitung Error
            forecast = model.predict(test_df[predict_cols])
            mae = mean_absolute_error(test_df["y"], forecast["yhat"])
            fold_maes.append(mae)

        tuning_results.append({"params": params, "mae": np.mean(fold_maes)})

    return tuning_results


