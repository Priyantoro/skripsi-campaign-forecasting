from statsmodels.tsa.stattools import adfuller

def test_stationarity(data, name):
    """
    Test stationarity menggunakan Augmented Dickey-Fuller (ADF) test.

    Hipotesis:
    - H0 (Null): Data memiliki unit root (non-stationary)
    - H1 (Alt): Data tidak memiliki unit root (stationary)

    Jika p-value < 0.05: Reject H0 → Data adalah stationary
    """
    result = adfuller(data)
    print(f"\n{name} Stationarity Test:")
    print(f"  ADF Statistic: {result[0]:.6f}")
    print(f"  p-value: {result[1]:.6f}")
    print(f"  Stationary: {'Yes' if result[1] < 0.05 else 'No'}")

# Test stationarity untuk kedua metrik
test_stationarity(daily_trend['CTR'], 'CTR')
test_stationarity(daily_trend['CPC'], 'CPC')
test_stationarity(daily_trend['clicks'], 'Clicks')

