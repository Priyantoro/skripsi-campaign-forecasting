    print(f"     • Avg overprediction: {np.mean(-residuals[residuals < 0]):.6f}")

    # Lagging detection
    max_lag_corr = 0
    best_lag = 0
    for lag in range(1, min(11, len(actual)//2)):
        corr = np.corrcoef(actual[lag:], pred[:-lag])[0, 1]
        if corr > max_lag_corr:
            max_lag_corr = corr
